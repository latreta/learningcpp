#include <iostream>
#include <math.h>

int somar(int a, int b) {
	return a + b;
}

int subtrair(int a, int b) {
	return a - b;
}

int multiplicar(int a, int b) {
	return a * b;
}

int dividir(int a, int b) {
	return a / b;
}


int potencia(int a, int potencia) {
	return pow(a, potencia);
}


int raizquadrada(int a) {
	return sqrt(a);
}